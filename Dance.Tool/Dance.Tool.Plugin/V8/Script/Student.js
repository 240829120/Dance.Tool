/**
 * 学生
 */
class Student {

    /**
     * 学生
     * @param {string} name 姓名
     * @param {number} age 年龄
     */
    constructor(name, age) {

        /**
         * 名称
         */
        this.name = name

        /**
         * 年龄
         */
        this.age = age
    }
}

export { Student }